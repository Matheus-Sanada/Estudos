let listNameNavs = ["Elementar", "Supernova", "Nabucodonosor"]
console.log(listNameNavs)

// 1 - Função (push("")) -> Serve para add um novo elemento na posicao final de um Array

listNameNavs.push("Estelar")
console.log(listNameNavs)

// 2 - Função (pop()) -> Serve para remover um elemento na posicao final de um Array

listNameNavs.pop()
console.log(listNameNavs)

// 3 - Função (shift()) -> Serve para remover um elemento de posicao primario em um Array

listNameNavs.shift()
console.log(listNameNavs)

// 4 - Função (unshift()) -> Serve para add um elemento em posicao primario em um Array.

listNameNavs.unshift("Matheus")
console.log(listNameNavs)

// 5 - Função (indexOf()) -> serve para sabermos qual índice determinado pedaço da string corresponde.

let positionsNameNav = listNameNavs.indexOf("Golias")
console.log(positionsNameNav)


