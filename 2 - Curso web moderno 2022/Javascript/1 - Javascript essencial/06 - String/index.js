const schoolName = "coder";

console.log(schoolName.charAt(3));
// O metodo (charAt(indice) mostra o indice e a letra recorente a aquele indice)

console.log(schoolName.charCodeAt(3))
// O charCodeAt()método retorna o Unicode do caractere em um índice especificado (posição) em uma string.

console.log(schoolName.substring(1))
// O metodo (substring(metodo) ele retorna a string a partir do metodo selecionado em diante)

console.log('ana,maria,matheus'.split(','))
// O metodo (split() é um separador)