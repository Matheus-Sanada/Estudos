
// Concatenação basica
console.log("planeta " + "terra")

// Extração de caracter de posição na string -> O primeiro indice de uma string é zero(0)
console.log("planeta terra" [3])

// Retornar o tamanho da string
console.log("planeta terra".length)

// Juntando operações

// Qunado as chaves estiver dentro do parenteses a string ira concatenar e dps ira retornar o tamanho da string concatenada.
console.log("Planeta " + "Terra"[3])

// Quando as chaves estiver fora do parenteses ele ira retonrar o tamanho da string normalmente.
console.log("Planeta " + "Terra")[3];


